# Math operations

# Addition
print(5 + 10) # Output: 15

# Subtraction
print(10 - 5) # Output: 5

# Multiplication
print(5 * 2) # Output: 10

# Division
print(5 / 2) # Output: 2.5

# Modulo (remainder)
print(5 % 2) # Output: 1

# Exponentiation (power of x)
print(5 ** 2) # Output: 25

# Floor division (Lowest truncated value - no decimals)
print (5 // 2) # Output: 2

# Booleans (work the same as any other language)
print(1 < 2) # Output: true
print(1 <= 2) # Output: true
print(1 > 2) # Output: false
print(1 >= 2) # Output: false
print(1 == 2) # Output: false

